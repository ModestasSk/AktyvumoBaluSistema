    public void Atnaujinti() {
        if (dropdown.getSelectedIndex() != -1 && !abs.getGrupes().isEmpty()) {
            Object[][] Studentai = new Object[(abs.gautiGrupe(dropdown.getSelectedItem().toString())).getStudentai().size()][3];
            int nr = 0;
            Object kodas, vardas, pavarde;
            for (Object o : abs.gautiGrupe(dropdown.getSelectedItem().toString()).getStudentai()) {
                kodas = ((Studentas) o).getKodas();
                vardas = ((Studentas) o).getVardas();
                pavarde = ((Studentas) o).getPavarde();
                Studentai[nr] = new Object[]{kodas, vardas, pavarde};
                nr++;
            }
            studLentele.setModel(new javax.swing.table.DefaultTableModel(
                    Studentai,
                    new String[]{
                        "Studento Kodas", "Vardas", "Pavardė"
                    }
            ));
        }
    }

