    private void f_kodasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_f_kodasActionPerformed
        try {
            int kodas = Integer.parseInt(f_kodas.getText());
            if (f_vardas.getText().length() != 0 && f_vardas.getText().length() <= 40 && f_pavarde.getText().length() != 0 && f_pavarde.getText().length() <= 40) {
                prideti.setEnabled(true);
            } else {
                prideti.setEnabled(false);
            }
        } catch (NumberFormatException e) {
            prideti.setEnabled(false);
        }
    }//GEN-LAST:event_f_kodasActionPerformed

