    private void pridetiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pridetiActionPerformed
        if (dropdown.getSelectedIndex() != -1 && !abs.getGrupes().isEmpty()) {
            String kodas = f_kodas.getText();
            String vardas = f_vardas.getText();
            String pavarde = f_pavarde.getText();
            Studentas studentas = new Studentas(Integer.parseInt(kodas), vardas, pavarde);
            Grupe grupe = abs.gautiGrupe(dropdown.getSelectedItem().toString());
            abs.priskirtiStudentaPrieGrupes(grupe, studentas);
            Atnaujinti();
        }
    }//GEN-LAST:event_pridetiActionPerformed

