    public void Atnaujinti() {
        Object[][] destytojai = new Object[abs.GautiDestytojuSkaiciu()][3];
        int nr = 0;
        Object kodas, vardas, pavarde;
        for (Object o : abs.getDestytojai()) {
            kodas = ((Destytojas) o).getKodas();
            vardas = ((Destytojas) o).getVardas();
            pavarde = ((Destytojas) o).getPavarde();
            destytojai[nr] = new Object[]{kodas, vardas, pavarde};
            nr++;
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
                destytojai,
                new String[]{
                    "Kodas", "Vardas", "Pavarde"
                }
        ));
    }

