    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if (Integer.parseInt(ivertinimas.getText()) > 10 || 0 > Integer.parseInt(ivertinimas.getText())) {
                l_ivertinimas.setForeground(Color.red);
            } else {
                l_ivertinimas.setForeground(Color.black);
            }
            if (komentaras.getText().length() > 250) {
                l_komentaras.setForeground(Color.red);
            }

            if (uzduotis.getIkelimai().size() != 0 && Integer.parseInt(ivertinimas.getText()) <= 10 && Integer.parseInt(ivertinimas.getText()) >= 0 && 250 >= komentaras.getText().length()) {
                ikelimas.setIvertinimas(Integer.parseInt(ivertinimas.getText()));
                ikelimas.setKomentaras(komentaras.getText());
                if (ikelimas != null) {
                    abs.AtnaujintiIvertinima(ikelimas);
                    l_ivertinimas.setForeground(Color.black);
                    l_komentaras.setForeground(Color.black);
                }
            }
        } catch (NumberFormatException e) {
            l_ivertinimas.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void bendrasKomentarasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bendrasKomentarasActionPerformed
         if (komentaras.getText().length() > 250) {
                l_komentaras.setForeground(Color.red);
            }

            if (uzduotis.getIkelimai().size() != 0 && Integer.parseInt(ivertinimas.getText()) <= 10 && Integer.parseInt(ivertinimas.getText()) >= 0 && 250 >= komentaras.getText().length()) {
                
                if (ikelimas != null) {
                    for(Ikelimas i: uzduotis.getIkelimai()){
                         i.setKomentaras(komentaras.getText());
                         abs.AtnaujintiIvertinima(i);
                    }
                }
            }
    }//GEN-LAST:event_bendrasKomentarasActionPerformed

