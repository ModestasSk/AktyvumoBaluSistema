    void AtnaujintiIkelima() {
        l_ivertinimas.setForeground(Color.black);
        l_komentaras.setForeground(Color.black);
        if (uzduotis.getIkelimai().size() != 0) {
            String strings[] = combo2.getSelectedItem().toString().split(" ");
            ikelimas = uzduotis.gautiIkelima(Integer.parseInt(strings[0]));
            String strings2[] = ikelimas.getStudentas().toString().split(" ");
            vardas.setText(strings2[1] + " " + strings2[2]);
            kodas.setText(strings[0]);
            komentaras.setText(ikelimas.getKomentaras());
            ivertinimas.setText(ikelimas.getIvertinimas() + "");
        }
    }

    void Atnaujinti() {
        l_ivertinimas.setForeground(Color.black);
        l_komentaras.setForeground(Color.black);
        tekstas.setText(uzduotis.getStatusas());
        pavadinimas.setText(uzduotis.getPavadinimas());
        aprasymas.setText(uzduotis.getAprasymas());
        if (!uzduotis.getIkelimai().isEmpty()) {
            list.removeAll();
            model.removeAllElements();

            for (Ikelimas u : uzduotis.getIkelimai()) {
                model.addElement(u);
            }
            list.setModel(model);
        }

    }

