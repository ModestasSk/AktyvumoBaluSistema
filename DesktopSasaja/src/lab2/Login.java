    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!abs.getDestytojai().isEmpty()) {
            String[] strings = combo.getSelectedItem().toString().split(" ");
            Destytojas destytojas = abs.gautiDestytoja(Integer.parseInt(strings[2].substring(1, strings[2].length() - 1)));
            abs.setDestytojas(destytojas);
            FrameUI frameUI = new FrameUI(abs);
            frameUI.setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void PrisijungtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrisijungtiActionPerformed
        if (!abs.getAdministratoriai().isEmpty()) {
            Administratorius administratorius = abs.gautiAdministratoriu(Integer.parseInt(combo2.getSelectedItem().toString()));
            char[] psw = administratorius.getSlaptazodis().toCharArray();
            if (Arrays.equals(psw, password.getPassword())) {
                FrameUIAdmin frameUI = new FrameUIAdmin(abs);
                frameUI.setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_PrisijungtiActionPerformed

