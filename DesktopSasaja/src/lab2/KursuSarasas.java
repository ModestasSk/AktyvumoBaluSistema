    public void Atnaujinti() {
        if (!abs.getKursai().isEmpty()) {
            Object[][] kursai = new Object[abs.getKursai().size()][2];
            int nr = 0;
            Object trump, pav, aprasymas;
            for (Object o : abs.getKursai()) {
                trump = ((Kursas) o).getTrumpasPav();
                pav = ((Kursas) o).getPavadinimas();
                kursai[nr] = new Object[]{trump, pav};
                nr++;
            }
            jTable1.setModel(new javax.swing.table.DefaultTableModel(
                    kursai,
                    new String[]{
                        "Trumpas pav.", "Pavadinimas"
                    }
            ));
        }
    }

