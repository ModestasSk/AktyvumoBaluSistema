    private void pridetiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pridetiActionPerformed
        if (!abs.getKursai().isEmpty()) {
            try {

                if (tf1.getText().isEmpty()) {
                    l_kodas.setForeground(java.awt.Color.red);
                } else {
                    l_kodas.setForeground(java.awt.Color.BLACK);
                }
                if (tf2.getText().isEmpty() || tf2.getText().length()>40) {
                    l_pavadinimas.setForeground(java.awt.Color.red);
                } else {
                    l_pavadinimas.setForeground(java.awt.Color.BLACK);
                }
                if (tf3.getText().isEmpty() || tf3.getText().length()>40) {
                    l_aprasymas.setForeground(java.awt.Color.red);
                } else {
                    l_aprasymas.setForeground(java.awt.Color.BLACK);
                }
                if (tf4.getText().length() != 10) {
                    l_data.setForeground(java.awt.Color.red);
                } else {
                    l_data.setForeground(java.awt.Color.BLACK);
                }
                if (!tf2.getText().isEmpty() && !tf3.getText().isEmpty() && tf4.getText().length() == 10) {
                    if (tf2.getText().length() <= 40 && tf3.getText().length() <= 40) {
                        Uzduotis uzduotis = new Uzduotis(Integer.parseInt(tf1.getText()), "nematomas", tf2.getText(), tf3.getText(), tf4.getText());
                        String[] strings = dropdown_kursas.getSelectedItem().toString().split(" ");
                        // abs.gautiKursa(strings[1].substring(1,strings[1].length()-1)).pridetiUzduoti(abs,uzduotis);
                        Kursas kursas = abs.gautiKursa(strings[1].substring(1, strings[1].length() - 1));
                        kursas.getUzduotys().add(uzduotis);
                        abs.priskirtiUzduotiPrieKurso(uzduotis, kursas);
                        Atnaujinti();
                        tf1.setText("");
                        tf2.setText("");
                        tf3.setText("");
                        tf4.setText("");

                        l_kodas.setForeground(java.awt.Color.BLACK);
                    }
                }
            } catch (NumberFormatException e) {
                l_kodas.setForeground(java.awt.Color.red);
            }
        }
    }//GEN-LAST:event_pridetiActionPerformed

    private void redaguotiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_redaguotiActionPerformed
        if (!list.isSelectionEmpty()) {
            String[] strings = dropdown_kursas.getSelectedItem().toString().split(" ");
            String uzd = model.getElementAt(list.getSelectedIndex()).toString();

            String[] strings2 = uzd.split(" ");
            Kursas kursas = abs.gautiKursa(strings[1].substring(1, strings[1].length() - 1));
            DarbasUzduotis darbasUzduotis = new DarbasUzduotis(abs, kursas.gautiUzduoti(Integer.parseInt(strings2[0])), this);
            darbasUzduotis.setVisible(true);
        }
    }//GEN-LAST:event_redaguotiActionPerformed

