    public void Atnaujinti() {
        if (!abs.getKursai().isEmpty()) {
            String[] strings = dropdown_kursas.getSelectedItem().toString().split(" ");
            list.removeAll();
            model.removeAllElements();
            for (Uzduotis u : abs.gautiKursa(strings[1].substring(1, strings[1].length() - 1)).getUzduotys()) {
                model.addElement(u);
            }
            list.setModel(model);
        }
    }

